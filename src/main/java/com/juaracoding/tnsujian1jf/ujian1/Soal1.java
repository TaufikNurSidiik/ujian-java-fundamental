package com.juaracoding.tnsujian1jf.ujian1;
/*
    Deskripsi:
    Buat sebuah program java yang menyimpan informasi tentang sebuah tiket bioskop.
    Program harus menyimpan nama film, harga tiket dan jumlah tiket yang dibeli.
    Contoh Output:
    Nama Film: Avengers: Endgame
    Harga Tiket: 50000
    Jumlah Tiket : 3
 */


public class Soal1 {
    public static void main(String[] args) {
        String namaFilm = "Avengers: Endgame";
        int hargaTiket = 50000;
        int jumlahTiket = 3;

        System.out.println("Nama Film: " + namaFilm);
        System.out.println("Harga Tiket: " + hargaTiket);
        System.out.println("Jumlah Tiket: " + jumlahTiket);
    }
}
